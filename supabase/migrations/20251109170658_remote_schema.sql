CREATE OR REPLACE FUNCTION public.atualizar_contas_vencidas()
 RETURNS void
 LANGUAGE sql
AS $function$
  update public.contas_a_pagar
  set status = 'Vencido'
  where
    status = 'Pendente' and data_vencimento < current_date;
$function$
;

CREATE OR REPLACE FUNCTION public.descontar_credito_funcionario(id_funcionario bigint, valor_desconto numeric)
 RETURNS void
 LANGUAGE plpgsql
AS $function$
BEGIN
  UPDATE public.funcionarios
  SET credito = credito - valor_desconto
  WHERE 
    id = id_funcionario
    AND credito >= valor_desconto; -- Condição de segurança para não deixar o crédito negativo

  IF NOT FOUND THEN
    RAISE EXCEPTION 'Saldo de crédito insuficiente ou funcionário não encontrado.';
  END IF;
END;
$function$
;

CREATE OR REPLACE FUNCTION public.finalizar_venda(id_da_venda bigint)
 RETURNS void
 LANGUAGE plpgsql
AS $function$
DECLARE
    item RECORD;
BEGIN
    -- 1. Itera sobre cada item da venda especificada
    FOR item IN
        SELECT produto_id, quantidade FROM public.venda_itens WHERE venda_id = id_da_venda
    LOOP
        -- 2. Dá baixa na quantidade do estoque na tabela de Produtos
        UPDATE public."Produtos"
        SET estoque = estoque - item.quantidade
        WHERE id = item.produto_id;
    END LOOP;

    -- 3. Atualiza o status da venda para 'Finalizada'
    UPDATE public.vendas
    SET status = 'Finalizada'
    WHERE id = id_da_venda;
END;
$function$
;

CREATE OR REPLACE FUNCTION public.get_dashboard_stats()
 RETURNS TABLE(total_produtos bigint, valor_total_estoque numeric, produtos_estoque_baixo bigint, produtos_sem_estoque bigint)
 LANGUAGE plpgsql
AS $function$
BEGIN
    RETURN QUERY
    SELECT
        (SELECT COUNT(*) FROM "Produtos" WHERE status = 'Ativo'),
        -- A CORREÇÃO ESTÁ AQUI: Adicionamos ::NUMERIC no final da soma
