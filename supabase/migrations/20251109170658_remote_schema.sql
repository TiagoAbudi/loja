CREATE OR REPLACE FUNCTION public.processar_entrada_de_produto(id_da_compra bigint)
 RETURNS void
 LANGUAGE plpgsql
AS $function$
DECLARE
    item RECORD;
BEGIN
    -- Itera sobre cada item da compra
    FOR item IN
        SELECT produto_id, quantidade, custo_unitario FROM public.compra_itens WHERE compra_id = id_da_compra
    LOOP
        -- Atualiza o estoque E o valor de custo do produto
        UPDATE public."Produtos"
        SET 
            estoque = estoque + item.quantidade,
            valor_de_custo = item.custo_unitario
        WHERE id = item.produto_id;
    END LOOP;
END;
$function$
;

