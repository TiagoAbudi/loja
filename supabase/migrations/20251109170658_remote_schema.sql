CREATE OR REPLACE FUNCTION public.get_lucratividade_periodo(data_inicio date, data_fim date)
 RETURNS TABLE(id_venda bigint, data_da_venda timestamp with time zone, cliente text, valor_da_venda numeric, custo_da_venda numeric, lucro numeric)
 LANGUAGE sql
AS $function$
  -- Usamos um CTE (WITH) para primeiro calcular o custo de cada venda
  WITH custos_por_venda AS (
    SELECT
      vi.venda_id,
      -- Soma a (quantidade * custo de cada produto) para achar o custo total da venda
      SUM(vi.quantidade * p.valor_de_custo) AS custo_total
    FROM public.venda_itens AS vi
    JOIN public."Produtos" AS p ON vi.produto_id = p.id
    -- Só considera itens que têm um valor de custo cadastrado
    WHERE p.valor_de_custo IS NOT NULL AND p.valor_de_custo > 0
    GROUP BY vi.venda_id
  )
  -- Agora, junta tudo
  SELECT
    v.id AS id_venda,
    v.data_venda AS data_da_venda,
    c.nome AS cliente,
    v.valor_liquido AS valor_da_venda,
    -- Se uma venda não teve custo (ex: produto sem custo cadastrado), considera como 0
    COALESCE(cpv.custo_total, 0) AS custo_da_venda,
    -- A mágica: Lucro = Valor da Venda - Custo da Venda
    (v.valor_liquido - COALESCE(cpv.custo_total, 0)) AS lucro
  FROM public.vendas AS v
  LEFT JOIN custos_por_venda AS cpv ON v.id = cpv.venda_id
  LEFT JOIN public."Clientes" AS c ON v.cliente_id = c.id
  WHERE v.status = 'Finalizada'
    AND v.data_venda BETWEEN data_inicio AND (data_fim + INTERVAL '1 day'); -- Pega o período inteiro
$function$
;

CREATE OR REPLACE FUNCTION public.get_or_create_cliente(nome_cliente text)
 RETURNS bigint
 LANGUAGE plpgsql
AS $function$
DECLARE
    cliente_id BIGINT;
BEGIN
    -- Tenta encontrar o cliente pelo nome (ignorando maiúsculas/minúsculas)
    SELECT id INTO cliente_id FROM public."Clientes" WHERE nome ILIKE nome_cliente LIMIT 1;

    -- Se não encontrou (a variável cliente_id está nula), cria um novo
    IF cliente_id IS NULL THEN
        INSERT INTO public."Clientes" (nome) VALUES (nome_cliente)
        RETURNING id INTO cliente_id;
    END IF;

    RETURN cliente_id;
END;
$function$
;

CREATE OR REPLACE FUNCTION public.get_or_create_fornecedor(nome_fornecedor text)
 RETURNS bigint
 LANGUAGE plpgsql
AS $function$
DECLARE
    fornecedor_id BIGINT;
BEGIN
    SELECT id INTO fornecedor_id FROM public.fornecedores WHERE nome_fantasia ILIKE nome_fornecedor LIMIT 1;

    IF fornecedor_id IS NULL THEN
        INSERT INTO public.fornecedores (nome_fantasia) VALUES (nome_fornecedor)
        RETURNING id INTO fornecedor_id;
    END IF;

    RETURN fornecedor_id;
END;
$function$
;

CREATE OR REPLACE FUNCTION public.get_relatorio_caixa_totalizadores(data_inicio date, data_fim date)
 RETURNS TABLE(total_vendas numeric, total_sangrias numeric, total_suprimentos numeric, diferenca_total numeric)
 LANGUAGE plpgsql
AS $function$
BEGIN
  RETURN QUERY
  SELECT
    -- Soma os valores de movimentações do tipo 'VENDA'
    COALESCE(SUM(cm.valor) FILTER (WHERE cm.tipo = 'VENDA'), 0) as total_vendas,
    -- Soma os valores de movimentações do tipo 'SANGRIA'
    COALESCE(SUM(cm.valor) FILTER (WHERE cm.tipo = 'SANGRIA'), 0) as total_sangrias,
    -- Soma os valores de movimentações do tipo 'SUPRIMENTO'
    COALESCE(SUM(cm.valor) FILTER (WHERE cm.tipo = 'SUPRIMENTO'), 0) as total_suprimentos,
    -- Soma a coluna 'diferenca' da própria tabela de caixas
    (SELECT COALESCE(SUM(c.diferenca), 0) FROM public.caixas c WHERE c.data_abertura::date BETWEEN data_inicio AND data_fim) as diferenca_total
  FROM
    public.caixa_movimentacoes cm
  WHERE
    cm.created_at::date BETWEEN data_inicio AND data_fim;
END;
$function$
;

CREATE OR REPLACE FUNCTION public.get_vendas_ultimos_7_dias()
 RETURNS TABLE(dia text, vendas numeric)
 LANGUAGE sql
AS $function$
  SELECT
    TO_CHAR(d.day, 'DD/MM') AS dia,
    COALESCE(SUM(v.valor_liquido), 0) AS vendas
  FROM
    -- CORREÇÃO APLICADA AQUI DENTRO
    generate_series(
      (CURRENT_DATE - INTERVAL '6 days')::date, -- Converte o início para data
      CURRENT_DATE::date,                      -- Converte o fim para data
      '1 day'
    ) AS d(day) -- Removemos o '::date' problemático daqui
  LEFT JOIN
    public.vendas AS v ON d.day = v.data_venda::date AND v.status = 'Finalizada'
  GROUP BY
    d.day
  ORDER BY
    d.day ASC;
$function$
;

